#include <iostream>
#include  <string>

using namespace std;

int main(){
    //creating variables
    int myNum = 5 ;//integer(whole number)
    float myFloat = 5.99; //floating point number 
    double myDoubleNum = 9.98; //floating point number

    char myLetter = 'D'; //character 
    bool myBoolean = true; //boolean
    string myString = "Hello"; //string

    //print the variables 
    cout <<"int: "<<myNum <<"\n"<< "Float: "<< myFloat<<"\n"<<"Double: "<< myDoubleNum<<"\n"; 
    cout <<"Char: "<<myLetter <<"\n"<< "Bool: "<< myBoolean<<"\n"<<"String: "<< myString<<"\n"; 

    return 0;
}